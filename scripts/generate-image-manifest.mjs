// Scans public/images for files named like "신해(辛亥).png" (한글(한자).ext)
// and writes src/data/iljuImages.ts mapping each Day Pillar's romanization key
// (e.g. "sinhae") to its public URL. Run after adding/renaming images:
//
//   npm run images
//
// Supported extensions: png, jpg, jpeg, webp, gif, avif.

import { readdirSync, writeFileSync, renameSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const STEM_HANJA = ['甲', '乙', '丙', '丁', '戊', '己', '庚', '辛', '壬', '癸'];
const STEM_ROMAN = ['gap', 'eul', 'byeong', 'jeong', 'mu', 'gi', 'gyeong', 'sin', 'im', 'gye'];
const BRANCH_HANJA = ['子', '丑', '寅', '卯', '辰', '巳', '午', '未', '申', '酉', '戌', '亥'];
const BRANCH_ROMAN = ['ja', 'chuk', 'in', 'myo', 'jin', 'sa', 'o', 'mi', 'sin', 'yu', 'sul', 'hae'];

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const imagesDir = join(root, 'public', 'images');
const outFile = join(root, 'src', 'data', 'iljuImages.ts');

const IMG_RE = /\.(png|jpe?g|webp|gif|avif)$/i;

function romanFromHanja(hanja) {
  const chars = [...hanja];
  if (chars.length !== 2) return null;
  const s = STEM_HANJA.indexOf(chars[0]);
  const b = BRANCH_HANJA.indexOf(chars[1]);
  if (s < 0 || b < 0) return null;
  return STEM_ROMAN[s] + BRANCH_ROMAN[b];
}

const files = readdirSync(imagesDir).filter((f) => IMG_RE.test(f));
const manifest = {};
const skipped = [];
const renamed = [];

for (const file of files) {
  // Pull the hanja out of parentheses: 신해(辛亥).png -> 辛亥
  const m = file.match(/[(（]([^)）]+)[)）]/);
  let roman = m ? romanFromHanja(m[1].trim()) : null;
  // Fallback: maybe the basename itself is already the romanization.
  if (!roman) {
    const base = file.replace(IMG_RE, '').toLowerCase();
    if (/^[a-z]+$/.test(base)) roman = base;
  }
  if (!roman) {
    skipped.push(file);
    continue;
  }

  // Normalize the extension and rename Korean/hanja files to ASCII romanization.
  // ASCII filenames sidestep macOS NFD/NFC normalization and URL-encoding issues
  // across local dev → Git → Vercel (Linux).
  const ext = '.' + file.split('.').pop().toLowerCase().replace(/^jpeg$/, 'jpg');
  const target = `${roman}${ext}`;
  if (file !== target) {
    if (existsSync(join(imagesDir, target))) {
      console.warn(`⚠ ${target} already exists — leaving "${file}" untouched.`);
    } else {
      renameSync(join(imagesDir, file), join(imagesDir, target));
      renamed.push(`${file} → ${target}`);
    }
  }
  manifest[roman] = `images/${target}`;
}

const sorted = Object.keys(manifest).sort();
const entries = sorted.map((k) => `  ${k}: ${JSON.stringify(manifest[k])},`).join('\n');

const body = `// AUTO-GENERATED by scripts/generate-image-manifest.mjs — do not edit by hand.
// Maps a Day Pillar romanization key (e.g. "sinhae") to its image URL under public/.
// Regenerate after adding images:  npm run images

export const ILJU_IMAGES: Record<string, string> = {
${entries}
};
`;

writeFileSync(outFile, body);

if (renamed.length) {
  console.log(`✓ Renamed ${renamed.length} file(s) to romanization:`);
  renamed.forEach((r) => console.log('   ' + r));
}
console.log(`✓ Wrote ${sorted.length} image(s) to src/data/iljuImages.ts`);
if (sorted.length) console.log('  ' + sorted.join(', '));
if (skipped.length) {
  console.warn(`⚠ Skipped (couldn't read a 한자 pillar from the name):`);
  skipped.forEach((f) => console.warn('   - ' + f));
}
